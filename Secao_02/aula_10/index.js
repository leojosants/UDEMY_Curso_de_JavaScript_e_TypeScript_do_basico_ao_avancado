// string
const nome = 'Leonardo';
console.log(`Tipo de dado: ${typeof(nome)}`);

// number
const numero = 1;
console.log(`Tipo de dado: ${typeof (numero)}`);

// undefined
let nomeAluno;
console.log(`Tipo de dado: ${typeof (nomeAluno)}`);

// object
const valorNulo = null;
console.log(`Tipo de dado: ${typeof (valorNulo)}`);

// boolean
const trabalhando = true;
console.log(`Tipo de dado: ${typeof (trabalhando)}`);